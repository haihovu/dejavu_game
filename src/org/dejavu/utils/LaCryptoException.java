public class LaCryptoException extends Exception {
	public LaCryptoException() {
		super();
	}
	public LaCryptoException(String msg) {
		super(msg);
	}
	public LaCryptoException(Throwable cause) {
		super(cause);
	}
	public LaCryptoException(String msg, Throwable cause) {
		super(msg, cause);
	}
}
